import fs from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import yaml from "js-yaml";

const __filename = fileURLToPath(import.meta.url);
const __dirname = path.dirname(__filename);
const repoRoot = path.resolve(__dirname, "../..");
const specPath = path.join(repoRoot, "apps/docs/openapi/v1/openapi.yaml");
const websiteBase = "https://ai-stats.phaseo.app";

const specRaw = await fs.readFile(specPath, "utf8");
const spec = yaml.load(specRaw);

const modelIds = spec?.components?.schemas?.ModelId?.enum;
if (!Array.isArray(modelIds) || modelIds.length === 0) {
	throw new Error("Could not find components.schemas.ModelId.enum in OpenAPI spec.");
}

/** @type {Array<{id: string; upper: string; pascal: string; url: string}>} */
const entries = [];
const usedUpper = new Map();
const usedPascal = new Map();

for (const rawId of modelIds) {
	if (typeof rawId !== "string") {
		continue;
	}
	const id = rawId.trim();
	if (!id) {
		continue;
	}
	const upper = uniqueName(toUpperSnake(id), usedUpper);
	const pascal = uniqueName(toPascal(id), usedPascal);
	entries.push({
		id,
		upper,
		pascal,
		url: modelPageUrl(id),
	});
}

await writeFile(
	"packages/sdk/sdk-ts/src/modelIds.ts",
	renderTs(entries)
);

await writeFile(
	"packages/sdk/sdk-py/src/ai_stats/model_ids.py",
	renderPy(entries)
);

await writeFile(
	"packages/sdk/sdk-go/model_ids.go",
	renderGo(entries)
);

await writeFile(
	"packages/sdk/sdk-csharp/ModelIds.cs",
	renderCsharp(entries)
);

await writeFile(
	"packages/sdk/sdk-java/src/ai/stats/sdk/ModelIds.java",
	renderJava(entries)
);

await writeFile(
	"packages/sdk/sdk-php/src/ModelIds.php",
	renderPhp(entries)
);

await writeFile(
	"packages/sdk/sdk-ruby/lib/ai_stats_sdk/model_ids.rb",
	renderRuby(entries)
);

await writeFile(
	"packages/sdk/sdk-rust/src/model_ids.rs",
	renderRust(entries)
);

await writeFile(
	"packages/sdk/sdk-cpp/src/gen/model_ids.hpp",
	renderCpp(entries)
);

console.log(`Generated model-id constants for ${entries.length} models.`);

function modelPageUrl(modelId) {
	const slashIndex = modelId.indexOf("/");
	if (slashIndex === -1) {
		return `${websiteBase}/models/${encodeURIComponent(modelId)}`;
	}
	const org = modelId.slice(0, slashIndex);
	const slug = modelId.slice(slashIndex + 1);
	return `${websiteBase}/models/${encodeURIComponent(org)}/${encodeURIComponent(slug)}`;
}

function toUpperSnake(value) {
	const normalized = value
		.replace(/[^a-zA-Z0-9]+/g, "_")
		.replace(/^_+|_+$/g, "")
		.toUpperCase();
	const out = normalized || "MODEL";
	return /^[A-Z_]/.test(out) ? out : `MODEL_${out}`;
}

function toPascal(value) {
	const parts = value.split(/[^a-zA-Z0-9]+/g).filter(Boolean);
	const normalized = parts
		.map((part) => part.charAt(0).toUpperCase() + part.slice(1).toLowerCase())
		.join("");
	const out = normalized || "Model";
	return /^[A-Z]/.test(out) ? out : `M${out}`;
}

function uniqueName(base, registry) {
	const current = registry.get(base) ?? 0;
	registry.set(base, current + 1);
	if (current === 0) {
		return base;
	}
	return `${base}_${current + 1}`;
}

async function writeFile(relativePath, contents) {
	const fullPath = path.join(repoRoot, relativePath);
	await fs.mkdir(path.dirname(fullPath), { recursive: true });
	await fs.writeFile(fullPath, contents, "utf8");
}

function renderHeader(commentPrefix) {
	return [
		`${commentPrefix} This file is generated by scripts/sdk/generate-model-id-constants.mjs.`,
		`${commentPrefix} Do not edit manually.`,
		"",
	].join("\n");
}

function renderTs(items) {
	const lines = [renderHeader("//").trimEnd(), "", 'import type { ModelId } from "./oapi-gen/models/ModelId.js";', "", "/** Known model ID constants for editor autocomplete and hover docs. */", "export const ModelIds = {",];

	for (const item of items) {
		lines.push(
			`  /** Model ID: \`${item.id}\`. Model page: ${item.url} */`,
			`  ${item.upper}: "${item.id}",`
		);
	}
	lines.push(
		"} as const satisfies Record<string, ModelId>;",
		"",
		"export const MODEL_IDS = Object.freeze(Object.values(ModelIds)) as readonly ModelId[];",
		"export const MODEL_ID_SET: ReadonlySet<ModelId> = new Set(MODEL_IDS);",
		""
	);
	return lines.join("\n");
}

function renderPy(items) {
	const lines = [renderHeader("#").trimEnd(), "", "from __future__ import annotations", "", "from typing import Final", "", '"""Known model ID constants for editor autocomplete and hover docs."""', "", "class ModelIds:", '    """Known model ID constants for editor autocomplete and hover docs."""', "",];

	for (const item of items) {
		lines.push(
			`    # Model ID: ${item.id}`,
			`    # Model page: ${item.url}`,
			`    ${item.upper}: Final[str] = "${item.id}"`,
			""
		);
	}

	lines.push(
		"MODEL_IDS: Final[tuple[str, ...]] = tuple(",
		"    getattr(ModelIds, name)",
		"    for name in dir(ModelIds)",
		'    if name.isupper() and isinstance(getattr(ModelIds, name), str)',
		")",
		"",
		"__all__ = [\"ModelIds\", \"MODEL_IDS\"]",
		""
	);
	return lines.join("\n");
}

function renderGo(items) {
	const lines = [renderHeader("//").trimEnd(), "", "package aistats", "", "import gen \"github.com/AI-Stats/AI-Stats/packages/sdk/sdk-go/src/gen\"", "", "// ModelIds contains known model IDs for editor autocomplete and hover docs.", "const (",];
	for (const item of items) {
		lines.push(
			`	// Model ID: ${item.id}`,
			`	// Model page: ${item.url}`,
			`	ModelID${item.pascal} gen.ModelId = "${item.id}"`,
		);
	}
	lines.push(")", "", "var KnownModelIDs = []gen.ModelId{");
	for (const item of items) {
		lines.push(`	ModelID${item.pascal},`);
	}
	lines.push("}", "");
	return lines.join("\n");
}

function renderCsharp(items) {
	const lines = [renderHeader("//").trimEnd(), "", "namespace AiStatsSdk", "{", "    /// <summary>Known model ID constants for editor autocomplete and hover docs.</summary>", "    public static class ModelIds", "    {",];
	for (const item of items) {
		lines.push(
			`        /// <summary>Model ID: <c>${item.id}</c>. Model page: ${item.url}</summary>`,
			`        public const string ${item.pascal} = "${escapeCsharp(item.id)}";`,
		);
	}
	lines.push("    }", "}", "");
	return lines.join("\n");
}

